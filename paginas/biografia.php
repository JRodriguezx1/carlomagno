                <h2 class="texto-bienvenida">Biografía<span>Fundación Carlo Magno</span></h2>
                <img loading="lazy" class="title-lineal" src="build/img/title-02.png" alt="">
                <div class="bloque-texto">
                    <p>La FUNDACIÓN EDUCATIVA ARTÍSTICA Y CULTURAL CARLOMAGNO, nace como iniciativa en el año 2015 en la ciudad de la Tebaida Quindío, resultado de un esfuerzo entre la comunidad del sector y líderes sociales del municipio, al principio fue una iniciativa recreada para ayudar a personas que no podían atender necesidades básicas en la educación ya que si estudiaban no podían trabajar, razón por la cual se empleó una metodología  flexible vinculante que fomentara estrategias pedagógicas que dieran paso a solucionar la dificultad del tiempo. </p>
                    <p>Durante algunos años la FUNDACIÓN EDUCATIVA ARTÍSTICA Y CULTURAL CARLOMAGNO aplicó la misma estrategia en otros municipios ayudando de esta manera incondicional a cientos de personas a resolver dicha coyuntura.</p>
                    <p>Para el 2020 la FUNDACIÓN EDUCATIVA ARTÍSTICA Y CULTURAL CARLOMAGNO cuenta con 3 sedes, armenia, yumbo y Pereira, generando empleabilidad y formación a las comunidades con recursos propios y aportes de sectores privados como tiendas, graneros, voluntarios entre otros.</p>
                    <p>Es así, como se inicia la solidificación de los estatutos y la razón social ante cámara y comercio de la ciudad de Cali, logrando para el 2022 su acreditación como entidad reconocida por el estado.</p>
                    <p>Actualmente cuenta con más de 7 sedes en todo el territorio Nacional lo que ha logrado consolidar una institución con prestigio realizando convenios con institutos técnicos y tecnológicos, colegios privados que aportan entre otras cosas su granito de arena. </p>
                    <p>Su fundador y presidente el señor Javier Gómez García impulsa proyectos conjuntamente con otras organizaciones educativas de carácter superior que puedan vincularse a la propuesta que viene manejando la fundación.</p>
